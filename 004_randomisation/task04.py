# defining a list of kenyan counbties
counties_of_kenya = ['Kiambu', 'Kirinyaga', 'Meru', 'Nyeri', 'Mombasa', 'Kilifi', 'Kwale']

# adding one county
counties_of_kenya.append('Nairobi')
print(counties_of_kenya)

# removing one county
counties_of_kenya.remove("Kilifi")
print(counties_of_kenya)

# add a county to a very specific location
counties_of_kenya.insert(0, 'Laikipia')
print(counties_of_kenya)
